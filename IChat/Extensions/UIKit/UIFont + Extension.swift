import UIKit

extension UIFont {
     
    static func avenir20() -> UIFont? {
        UIFont.init(name: "avenir", size: 20)
    }
    
    static func avenir26() -> UIFont? {
        UIFont.init(name: "avenir", size: 26)
    }
    
    static func laoSangamMN20() -> UIFont? {
        UIFont.init(name: "Lao Sangam MN", size: 20)
    }
    
    static func laoSangamMN18() -> UIFont? {
        UIFont.init(name: "Lao Sangam MN", size: 18)
    }
}
